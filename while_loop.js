/////////////// WHILE LOOP ////////////////////

var number;

number = 1;

while (number < 10) {
    console.log(number);
    number++;
}

var x;

x = 10;

while (x < 20) {
    console.log(x);
    x += 2;
}

var a, b;

a = 20;
b = 100;

while (a <= b) {
    if (a % 5 === 0 && a % 7 === 0) {
        console.log(a);
    }
    a++;
}